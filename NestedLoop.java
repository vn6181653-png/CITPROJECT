public class NestedLoop {
    public static void main(String[] args) {
        for(int i=1;i<=2;i++){
            System.out.println("Tiger:");
            for(int j=1;j<=2;j++){
                System.out.println("   Lion");
            }
            for(int k=1;k<=3;k++) {
                System.out.println("cat");
            }     
        }     
    }
    
}
